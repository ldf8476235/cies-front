<script>
import Func from '@/components/seach-func-header/Func.vue'
export default {
  name: 'Task',
  components: {
    Func
  },
  data() {
    return {
      text: '新建任务',
      total: 0,
      pageSize: 10,
      currPage: 1,
      // 选择项内容
      options: [
        {
          value: '选项1',
          label: '黄金糕'
        },
        {
          value: '选项2',
          label: '双皮奶'
        }
      ],
      selectVal: '', // 选中项
      inputKey: '', // 搜索输入项
      taskList: [] // 任务列表
    };
  },
  mounted() {
    this.getTaskList(this.currPage, this.pageSize)
  },
  methods: {
    // 获取所有任务
    getTaskList(page, size) {
      const url = `task/list/?page=${page}&limit=${size}`
      this.$http.get(url).then(res => {
        console.log(res)
        if (res.code === 1) {
          this.taskList = res.data.list
          this.total = res.data.totalCount
        }
      })
    },
    // 编辑
    edit(row) {
      this.$router.push(
        {
          name: 'NewTask',
          query: {
            editId: row.taskId
          },
          params: {
            data: row
          }
        }
      )
    },
    // 单个删除任务
    del(id) {
      const url = `task/delete/${id}`
      this.$http.delete(url).then(res => {
        if (res.code === 1) {
          this.getTaskList(this.currPage, this.pageSize)
          this.$message({
            type: 'success',
            message: '删除成功！'
          })
        }
      })
    },
    // 新建任务
    goNewTask() {
      this.$router.push('/task/newtask');
    },
    // 当前页条数
    handleSizeChange(size) {
      this.pageSize = size
      this.getTaskList(this.currPage, size)
    },
    // 当前页面
    handleCurrChange(page) {
      this.currPage = page
      console.log(this.currPage)
      this.getTaskList(page, this.pageSize)
    }
  }
};
</script>

